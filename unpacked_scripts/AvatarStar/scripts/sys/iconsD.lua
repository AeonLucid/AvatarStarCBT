function GetLoadingMap(mapName, modeName)
  local map_name = string.lower(mapName)
  if not mapName or not map_name_to_loading_pic[map_name] then
    print("Error: map name not valid", mapName)
    return nil
  end
  return Gui.Image(string.format("/ui/MapsAndBG/Loading/%s", map_name_to_loading_pic[map_name]))
end

function GetTeamLogo(logoName)
  local index = tonumber(logoName)
  if index and 0 < index and index < 6 then
    return TeamLogos[index]
  else
    return TeamLogos[1]
  end
end

